n = int(input())
# n = 25
x = n % 10
if 10 <= n <= 20 or 5 <= x <= 9 or x == 0:
    m = ''
elif x == 1:
    m = 'a'
else:
    m = 'y'
print(n, ' ', 'korov', m, sep='')

# "n коров", если 10 < n < 20 или последняя цифра n - одна из 0, 5, 6, 7, 8, 9.
# "n корова", если последняя цифра n == 1.
# "n коровы" во всех остальных случаях.
